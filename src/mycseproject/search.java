            // JOptionPane.showMessageDialog(null,"SORRY THE ROLL DOESNOT EXIST");
        int p=0;
        String s1;
         try
        {
            String url="jdbc:derby://localhost:1527/cgpa";
            String username="ansarul";
            String password="12345";
            Connection con=DriverManager.getConnection(url,username,password);
            Statement st=con.createStatement();
            String s="SELECT * FROM RESULT WHERE ROLL='"+search.getText()+"'";
            ResultSet rs=st.executeQuery(s);
            while(rs.next())
            {
                    s1=rs.getString(2);
                    if (s1.equals(search.getText()))
                    {
                        p=1;break;
                    }
                    else 
                        p=0;
            }
            if (p==1)
            {
                name.setText(rs.getString(1));
                roll.setText(rs.getString(2));
                cgpa.setText(rs.getString(3));
            }
            else 
            {
                JOptionPane.showMessageDialog(null,"The Roll number does not exist..");
            }
        }
         catch(SQLException ex)
         {
             JOptionPane.showMessageDialog(null,ex.toString());
         }
        
                    
